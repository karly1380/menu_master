<p class="lead">Los peces betta son nativos de Asia, donde viven en aguas poco profundas de pantanos, estanques o corrientes lentas. Los machos son padres devotos que construyen con sus bocas los nidos para sus bebés y los protegen ferozmente contra los predadores. Al igual que nosotros, los peces betta son diurnos, lo que significa que están activos durante el día y duermen por la noche, osea que necesitan de la oscuridad para tener un buen descanso por la noche.
  <br>
  <br>

Si bien algunos bettas son capturados en la naturaleza, la mayoría de aquellos vendidos en EE.UU. provienen de granjas de cría en Tailandia y otros países en el Sudeste Asiático, donde es práctica común tenerlos en pequeñas botellas. Cuando es hora de empacarlos para transportarlos a EE.UU., los peces son arrojados descuidadamente a cestas cubiertas con redes y colocados en pequeños vasos de plástico.
