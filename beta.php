<p class="lead">Cómo preparar una pecera para peces betta
<br>
Preparar y mantener un acuario apropiado para cualquier especie de pez es mucho más complejo, costoso y ocupa mucho más tiempo de lo que la gente cree, por eso PETA sugiere no comprar nunca peces como «mascotas». Si ya tienes bettas, puedes hacerles la vida más fácil brindándoles un entorno que sea lo más parecido posible a su hábitat natural. Si bien los peces en cautiverio nunca pueden disfrutar una vida natural, los siguientes consejos ayudarán a que sean lo más felices posible.

<br>
<br>

Generalmente, ningún pez debería estar en un tanque de menos de 10 galones. Sin embargo, un solo betta puede vivir en un tanque de 5 galones, pero algo más grande siempre es mejor. En casi todas partes, el agua del grifo contiene cloro y cloramina, lo cual matará a los peces, por eso debes tratarla apropiadamente con un acondicionador de agua antes de colocarla en el acuario. Una vez que la pecera ya está preparada con un filtro que funcione apropiadamente (los bettas prefieren espacios con flujo bajo), sustratos y decoraciones, el agua debe ser reciclada durante varias semanas antes de introducir los peces.
