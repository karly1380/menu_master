<p class="lead">La temperatura del agua debería mantenerse entre 76 y 82 grados Fahrenheit. Por esta razón, los bettas jamás deberían ser puestos en un tanque junto a peces dorados, quienes necesitan agua más fría para estar cómodos. El acuario debería estar en un lugar donde la temperatura y la luz fueran constantes y controlables. Ten en cuenta que las plantas vivas puestas en la parte superior de contenedores pequeños pueden restringir el acceso que los bettas necesitan para obtener oxígeno, ya que toman el oxígeno de la superficie del agua.
